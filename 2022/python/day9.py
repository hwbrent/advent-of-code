class Direction(Enum):
    U = "U"
    D = "D"
    L = "L"
    R = "R"

    @staticmethod
    def parse(s):
        match s:
            case "U":
                return Direction.U
            case "D":
                return Direction.D
            case "L":
                return Direction.L
            case "R":
                return Direction.R
            case _:
                return None

    @staticmethod
    def get_xory(d: 'Direction'):
        if d == Direction.L or d == Direction.R:
            return 0
        elif d == Direction.U or d == Direction.D:
            return 1
    
    @staticmethod
    def get_factor(d):
        if d == Direction.U or d == Direction.R:
            return 1
        elif d == Direction.D or d == Direction.L:
            return -1

def close_enough(tail, head) -> 'bool':
    ''' Indicates whether `tail` is within 1 space of `head`. '''
    return tail[0] in range(head[0]-1, head[0]+2) and tail[1] in range(head[1]-1, head[1]+2)

def part1(steps):
    tail = [0,0]
    head = [0,0]

    visited = set()
    visited.add((0,0))

    for direction, magnitude in steps:

        xory = Direction.get_xory(direction)
        factor = Direction.get_factor(direction)

        for _ in range(magnitude):
            head_prev = copy.deepcopy(head)
            head[xory] += factor

            # `tail` only needs to move when it's not within 1 space of `head`
            # after `head` has done its move.
            if not close_enough(tail, head):
                tail = copy.deepcopy(head_prev)
                visited.add(tuple(tail))

    print('Part 1 -->', len(visited))

